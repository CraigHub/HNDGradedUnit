     //Passing in staff class and order selected from Staff order to load order
    public StaffOrderLines(Staff staff, Order order) {
        //Linking the staff class to the attribute
        loggedInStaff = staff;
        initComponents();
        //Set background colour
        getContentPane().setBackground(new java.awt.Color(255, 216, 57));
        
        //This displays the orders in the table, one for each row
        tblAdminOrders.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        //Looks at the orderlines created and finds information about the order
        DefaultTableModel model = (DefaultTableModel)tblAdminOrders.getModel();
        //Loops through the orderline to get all items on the order
        for(Map.Entry<Integer, OrderLine> olEntry : 
                order.getOrderLines().entrySet())
        {
            //Gets information of the order
            OrderLine actualOrderLine = olEntry.getValue();
            //Gets the product information
            Product orderedProduct = actualOrderLine.getProduct();
          //Adds the row to the table, displaying product id, name and price
            model.addRow(new Object[]{orderedProduct.getProductId(), orderedProduct.toString(),
                            "Â£" + orderedProduct.getPrice()});
        
    }
    }

