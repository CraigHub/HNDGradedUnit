    //Passing the Customer and OrderId in
    public CustomerOrderLines(Customer customerIn, int orderIdIn) {
        //Connecting the customer class to the atribute
        loggedInCustomer = customerIn;
        
        //This gets the orders made by the customer using the OrderId
        Order order = loggedInCustomer.getOrders().get(orderIdIn);
        initComponents();
        //Sets background
        getContentPane().setBackground(new java.awt.Color(255, 216, 57));
       
        //This will display the orders in the table, one row at a time
        tblOrderLines.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        //Looks at th orderlines created and finds information about the order and product
        DefaultTableModel model = (DefaultTableModel)tblOrderLines.getModel();
        for(Map.Entry<Integer, OrderLine> olEntry : 
                order.getOrderLines().entrySet())
        {
            //Get orderline value and Get product
            OrderLine actualOrderLine = olEntry.getValue();
            Product orderedProduct = actualOrderLine.getProduct();
          //This will add to the table displaying the product ID, name and price
            model.addRow(new Object[]{orderedProduct.getProductId(), orderedProduct.toString(),
                            "Â£" + orderedProduct.getPrice()});
        
    }
    }

